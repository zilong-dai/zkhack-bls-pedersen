fn main() {
    welcome();
    puzzle(PUZZLE_DESCRIPTION);
    let (pk, _ms, sigs) = puzzle_data();

    let selectors = get_selectors();
    let mut sum = G1Projective::zero();
    for (i, num) in selectors.iter().enumerate() {
        let additive = sigs[i].into_projective().mul(num);
        sum += additive;
    }
    let sig_acc = G1Affine::from(sum);
    let msg = b"minmin";
    verify(pk, msg, sig_acc);

    println!("the sig of msg \"{}\" is {{ {} {} }}", std::str::from_utf8(msg).unwrap(), sig_acc.x.to_string(), sig_acc.y.to_string());
}

